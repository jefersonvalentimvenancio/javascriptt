// atribuiçao
let a = 10;
let b = 5;
let c = 2;  
// Atribuição simples
a = b; // a agora é igual a b (5)
// Atribuição composta
a += c; // a agora é igual a b + c (5 + 2 = 7)
a -= c; // a agora é igual a b - c (5 - 2 = 3)
a *= c; // a agora é igual a b * c (5 * 2 = 10)
a /= c; // a agora é igual a b / c (5 / 2 = 2.5)
a %= c; // a agora é igual ao resto da divisão de b por c (5 % 2 = 1)
a **= c; // a agora é igual a b elevado a c (5 ** 2 = 25)